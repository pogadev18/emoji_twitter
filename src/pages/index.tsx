import { type NextPage } from "next";
import { SignInButton, useUser } from "@clerk/nextjs";
import Image from "next/image";

import Head from "next/head";
import { api } from "~/utils/api";
import type { RouterOutputs } from "~/utils/api";

const CreatePostWizard = () => {
  const { user } = useUser();

  console.log("user >>", user);

  if (!user) return null;

  return (
    <div className="flex gap-3">
      <Image
        width="60"
        height="60"
        className="rounded-full"
        src={user.profileImageUrl}
        alt="profile image"
      />
      <input
        type="text"
        placeholder="type some emojis"
        className="w-full bg-transparent outline-none"
      />
    </div>
  );
};

type PostWithUser = RouterOutputs["posts"]["getAll"][number];
const PostView = (props: PostWithUser) => {
  const { content, author } = props;

  return <article className="border-b border-slate-400 p-8">{content}</article>;
};

const Home: NextPage = () => {
  const user = useUser();
  const { data: posts, isLoading: postsLoading } = api.posts.getAll.useQuery();

  if (postsLoading) return <p>loading posts...</p>;
  if (!posts) return <p>something went wrong</p>;

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex h-screen justify-center">
        <div className="w-full border-x border-slate-400 md:max-w-2xl">
          <div className="border-b border-slate-400 p-4">
            {!user.isSignedIn && (
              <SignInButton mode="modal">
                <button className="btn">Sign in</button>
              </SignInButton>
            )}
            {user.isSignedIn && <CreatePostWizard />}
          </div>
          <div className="flex flex-col">
            {posts.map((post) => (
              <PostView key={post.id} {...post} />
            ))}
          </div>
        </div>
      </main>
    </>
  );
};

export default Home;
